package challenge;

public class URLToAnalyse {

	private String url;
	private int range;

	public URLToAnalyse() {

	}

	public URLToAnalyse(String url, int range) {
		this.url = url;
		this.range = range;
	}

	public String getUrl() {
		return url;
	}

	public int getRange() {
		return range;
	}

}
